void OLED_P6x8Str(unsigned char x, unsigned char y,unsigned char ch[])
{
	unsigned char c=0,i=0,j=0;

	while (ch[j]!='\0')
	{
		c =ch[j]-32;
		if(x>126){x=0;y++;}
		OLED_Set_Pos(x-1,y);
		for(i=0;i<6;i++)
		ssd1306_write_data(F6x8[c][i]);
		x+=6;
		j++;
	}
}
